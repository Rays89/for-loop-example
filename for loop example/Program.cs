using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace for_loop_example
{
    class Program
    {
        static void Main(string[] args)
        {


            int sum = 0;

            for (int i = 1; i <= 5000; i++)
            {
                 sum += i;
            }
            
            Console.WriteLine(sum);









        }

       
            
    }
}
 